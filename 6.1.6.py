class Peekable:
    def __init__(self, iterable):
        self.iterator = iter(iterable)
        self._next = next(self.iterator, None)

    def __iter__(self):
        return self

    def __next__(self):
        if self._next is not None:
            result = self._next
            self._next = next(self.iterator, None)
            return result
        else:
            raise StopIteration

    def peek(self, default='z'):
        if self._next is not None:
            return self._next
        elif default !='z':
            return default
        else:
            raise StopIteration        

