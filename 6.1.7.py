            raise AttributeError ('–ò—Å—Ö–æ–¥–Ω—ã–π –∏—Ç–µ—Ä–∏—Ä—É–µ–º—ã–π –æ–±—ä–µ–∫—Ç –ø—É—Å—Ç')
            
